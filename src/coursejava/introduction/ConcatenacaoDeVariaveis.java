package coursejava.introduction;

public class ConcatenacaoDeVariaveis {
    public static void main(String[] args) {

        //concatenacao é igual a unir ou juntar dados, usando "+"
        String nome = "Valeska Marques";
        String cpf = "123.456.789.00";
        String telefone = "12345-6789";
        String endereco = "Brooklyn - EUA";
        int idade = 19;

        System.out.println("meu nome é " + nome + " e cpf: " +
                cpf + " meu contato é: " + telefone + " e moro em " +
                endereco + " e minha idade é " + idade);

        //ou posso concatenar em uma variável
    }
}
